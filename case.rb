weather = 'sunny'
case weather
when 'rainy'
  puts "Take an umbrella with you!"
when 'cloudy'
  puts "Leave the sunglasses at home!"
when 'windy'
  puts "Che bello! Enjoy the wind and take a scarf with you!"
when 'sunny'
  puts "Don't forget your sunglasses!"
when 'snowy'
  puts "Get your winter-boots!"
else
  puts" No idea!"
end